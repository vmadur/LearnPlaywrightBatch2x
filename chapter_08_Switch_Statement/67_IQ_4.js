
let value = "5";
console.log(typeof value);
switch (value) {
    case 5:
        console.log("Number 5 matched");
        break;
    case 5:
        console.log("String '5' matched");
        break;
}

let status = 0;
console.log(typeof status)
switch (status) {
    case false:
        console.log("false matched");
    case 0:
        console.log("0 matched");
        break;
}