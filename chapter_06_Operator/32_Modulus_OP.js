// % = Modulus operator (remainder)
let a = 10;
let b = 3;
let remainder = a % b;
console.log(remainder); // 1

let c = 15;
let d = 4;
let remainder2 = c % d;
console.log(remainder2); // 3   

let result = 13 % 7;
console.log(result); // 6       

console.log(101 % 2); // 1
console.log(100 % 2); // 0      
// n%2 === 0  => even number
// n%2 === 1  => odd number 
console.log(37 % 2); // 1
console.log(36 % 2); // 0       

