let x = 10;
x += 10; // x = x + 10
console.log(x); // 20   
// these are compound assignment operators, they combine an arithmetic operation with assignment.

x -= 5; // x = x - 5
console.log(x); // 15
x *= 2; // x = x * 2
console.log(x); // 30
x /= 3; // x = x / 3
console.log(x); // 10
x %= 4; // x = x % 4
console.log(x); // 2
x **= 3; // x = x ** 3
console.log(x); // 8 => 2 * 2 * 2 = 8   

